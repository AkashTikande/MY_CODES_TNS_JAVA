public class JavaBasics_Loops_Nested_For_Loop {
    public static void main(String[] args) {
        int begin = 10;
        int end = 20;
        for (int i = begin; i <= end; i++)
        {
            for(int j =1; j<10;j++)
            {
                System.out.println(i+""+j+"=");   //complete it from TNS GITHUB.
            }
        }

    }
}
